void main() {
  int n1 = 10;
  int n2 = 13;
  int n3 = 11;
  int n4 = 2;

  //igual ==
  bool ig = n1 == 10;
  bool r1 = n1 == n2;
  //diferente !=
  bool dif = n1 != 10;
  bool r2 = n1 != n2;
  // maior <
  bool r3 = n1 < n2;
  //menor >
  bool r4 = n1 > n2;
  //maior ou igual
  bool r5 = n3 >= n4;
  //menor ou igual
  bool r6 = n4 <= n3;

  print(ig);
  print(dif);
  print(r1);
  print(r2);
  print(r3);
  print(r4);
  print(r5);
  print(r6);
}
